export class Wish{
    username:string;
    Category: String;
    Breed: String;
    Color: String;
    Gendar: String;
    Age: String;
    SpecialR: String;



constructor(username:string,Category: String, Breed: String, Color: String, Gendar: String, Age: String, SpecialR: String){
    this.username=username;
    this.Category = Category;
    this.Breed = Breed;
    this.Color = Color;
    this.Gendar = Gendar;
    this.Age = Age;
    this.SpecialR = SpecialR;
}


}